            Test.Check(solution.reverseBits, 0b01000000000000000000000000000001u, 0b10000000000000000000000000000010u);
            Test.Check(solution.reverseBits, 0b01000000000000000000000000000010u, 0b01000000000000000000000000000010u);
            Test.Check(solution.reverseBits, 0b00000000000000000000000000000011u, 0b11000000000000000000000000000000u);
            Test.Check(solution.reverseBits, 0b10000000000000000000000000000011u, 0b11000000000000000000000000000001u);
