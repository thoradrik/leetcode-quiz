namespace Quiz {
    public class Solution {
        
        public int HammingWeight(uint n) {
            uint a1 = n         & 0b01010101010101010101010101010101u;
            uint a2 = (n >> 1)  & 0b01010101010101010101010101010101u;
            uint b = a1 + a2;
            uint b1 = b         & 0b00110011001100110011001100110011u;
            uint b2 = (b >> 2)  & 0b00110011001100110011001100110011u;
            uint c = b1 + b2;
            uint c1 = c         & 0b00001111000011110000111100001111u;
            uint c2 = (c >> 4)  & 0b00001111000011110000111100001111u;
            uint d = c1 + c2;
            uint e1 = d         & 0b00000000111111110000000011111111u;
            uint e2 = (d >> 8)  & 0b00000000111111110000000011111111u;
            uint f = e1 + e2;
            uint f1 = f         & 0b00000000000000001111111111111111u;
            uint f2 = (f >> 16) & 0b00000000000000001111111111111111u;
            return (int)(f1 + f2);
        }
        
    }
}